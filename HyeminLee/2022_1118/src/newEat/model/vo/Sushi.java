package newEat.model.vo;

public class Sushi extends Food {
	
	
	public Sushi() {}
	
	
	public Sushi(int num, String name) {
		super(num,name);
	}
	
	
	public Sushi(int num, String name,int price) {
		super(num,name,price);
	}
	

	@Override
	public String end() {
		return super.end();
	}
	
	
	@Override
	public String toString() {
		return super.toString();
	}
	

}
